package br.com.nutri.model;

public class AvaliacaoAntropometrica {
	
	private float peso;
	private float altura;
	private String circunferenciaBraco;
	private String circunferenciaCintura;
	private String cicAbdomem;
	private String cicQuadri;
	private String cicPerna;
	private float porcentagemGordura;
	private float porcentagemMassaMagra;
	private int idadeMetabolica;
	public float getPeso() {
		return peso;
	}
	public void setPeso(float peso) {
		this.peso = peso;
	}
	public float getAltura() {
		return altura;
	}
	public void setAltura(float altura) {
		this.altura = altura;
	}
	public String getCircunferenciaBraco() {
		return circunferenciaBraco;
	}
	public void setCircunferenciaBraco(String circunferenciaBraco) {
		this.circunferenciaBraco = circunferenciaBraco;
	}
	public String getCircunferenciaCintura() {
		return circunferenciaCintura;
	}
	public void setCircunferenciaCintura(String circunferenciaCintura) {
		this.circunferenciaCintura = circunferenciaCintura;
	}
	public String getCicAbdomem() {
		return cicAbdomem;
	}
	public void setCicAbdomem(String cicAbdomem) {
		this.cicAbdomem = cicAbdomem;
	}
	public String getCicQuadri() {
		return cicQuadri;
	}
	public void setCicQuadri(String cicQuadri) {
		this.cicQuadri = cicQuadri;
	}
	public String getCicPerna() {
		return cicPerna;
	}
	public void setCicPerna(String cicPerna) {
		this.cicPerna = cicPerna;
	}
	public float getPorcentagemGordura() {
		return porcentagemGordura;
	}
	public void setPorcentagemGordura(float porcentagemGordura) {
		this.porcentagemGordura = porcentagemGordura;
	}
	public float getPorcentagemMassaMagra() {
		return porcentagemMassaMagra;
	}
	public void setPorcentagemMassaMagra(float porcentagemMassaMagra) {
		this.porcentagemMassaMagra = porcentagemMassaMagra;
	}
	public int getIdadeMetabolica() {
		return idadeMetabolica;
	}
	public void setIdadeMetabolica(int idadeMetabolica) {
		this.idadeMetabolica = idadeMetabolica;
	}
	
	
	

}
